ir_hacia(X, Y, Acc, Route):-
    abolish(eslabon, 1),
    assert(eslabon(X)),
    ir_hacia2(X, Y, Acc, Route).

ir_hacia2(X, Y, Acc, Route):-
    conecta(X, Y),
    reverse([Y|Acc], Route),
    assert(eslabon(Y)),
    !.

ir_hacia2(X, _, _, _):-
    conecta(X, Z),
    eslabon(Z),
    fail.

ir_hacia2(X, Y, Acc, Route):-
    conecta(X, Z),
    not(eslabon(Z)),
    assert(eslabon(Z)),
    ir_hacia2(Z, Y, [Z|Acc], Route),
    !.

